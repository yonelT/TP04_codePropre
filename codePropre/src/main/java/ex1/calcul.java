public class Calcul {

	/** Sert a faire des additions
	 * 
	 * @param int a: premier numero
	 * @param int b: deuxieme numero
	 * @return int : resultat de l'addition
	 */
	public int addition(int a, int b){
		return a+b;
	}
	
	/** Sert a faire des soustractions
	 * @param int a: premier numero
	 * @param int b: deuxieme numero
	 * @return int: resultat de la soustraction
	 */
	public int soustraction(int a, int b){
		return a-b;
	}
}
